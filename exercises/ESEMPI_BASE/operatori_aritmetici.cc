using namespace std;
#include <iostream>

int main ()        
{
  
  int n = 7;
  int m = 3;

  cout << "n = " << n << endl;    
  cout << "m = " << m << endl;    
  cout << "n+m = " << n+m << endl;
  cout << "n-m = " << n-m << endl;
  cout << "n*m = " << n*m << endl;
  cout << "n/m = " << n/m << endl;
  cout << "n%m = " << n%m << endl;
  cout << "-(n-m) = " << -(n-m) << endl;
  cout << "-n-m = " << -n-m << endl;


return 0;
}
 

