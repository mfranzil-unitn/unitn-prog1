using namespace std;
#include <iostream>

int main ()        
{

  cout << "\\n significa: " << "[\n]" << endl;
  cout << "\\t significa: " << "[\t]" << endl;
  cout << "\\v significa: " << "[\v]" << endl;
  cout << "\\b significa: " << "[\b]" << endl;
  cout << "\\r significa: " << "[\r]" << endl;
  cout << "\\f significa: " << "[\f]" << endl;
  cout << "\\a significa: " << "[\a]" << endl;
  cout << "\\\\ significa: " << "[\\]" << endl;
  cout << "\\\' significa: " << "[\']" << endl;
  cout << "\\\" significa: " << "[\"]" << endl;

return 0;
}
