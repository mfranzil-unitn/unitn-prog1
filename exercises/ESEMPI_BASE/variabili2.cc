using namespace std;
#include <iostream>

int main ()        
{
  
  int n=10;
  float pi = 3.1415;    // initializzazione
  float duepi = pi*2;
  char c = 'C';
 
  cout << "n = " << n << endl;
  cout << "pi = " << pi << endl;
  cout << "duepi = " << duepi << endl;
  cout << "c = " << c << endl;

return 0;
}
 

